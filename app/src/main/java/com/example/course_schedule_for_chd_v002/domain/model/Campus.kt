package com.example.course_schedule_for_chd_v002.domain.model

/**
 * 校区枚举 (v61)
 * 每个校区有不同的上课时间安排
 *
 * 渭水校区：
 * - 上午：8:30-12:00（大课间20分钟，小课间5分钟）
 * - 下午：14:00-17:30
 * - 晚上：19:00-21:25（课间5分钟）
 *
 * 本部校区：
 * - 上午：8:00-11:50（大课间30分钟，小课间10分钟）
 * - 下午：14:00-17:40
 * - 晚上：19:00-21:35（课间10分钟）
 */
enum class Campus(val displayName: String, val timeSlots: List<String>) {
    WEISHUI("Weishui", listOf(
        "8:30-9:15",    // 第1节
        "9:20-10:05",   // 第2节
        "10:25-11:10",  // 第3节
        "11:15-12:00",  // 第4节
        "14:00-14:45",  // 第5节
        "14:50-15:35",  // 第6节
        "15:55-16:40",  // 第7节
        "16:45-17:30",  // 第8节
        "19:00-19:45",  // 第9节
        "19:50-20:35",  // 第10节
        "20:40-21:25"   // 第11节
    )),
    BENBU("Benbu", listOf(
        "8:00-8:45",    // 第1节
        "8:55-9:40",    // 第2节
        "10:10-10:55",  // 第3节
        "11:05-11:50",  // 第4节
        "14:00-14:45",  // 第5节
        "14:55-15:40",  // 第6节
        "16:10-16:55",  // 第7节
        "17:05-17:40",  // 第8节
        "19:00-19:45",  // 第9节
        "19:55-20:40",  // 第10节
        "20:50-21:35"   // 第11节
    ));

    companion object {
        /**
         * 从名称获取校区
         * @param name 校区名称（WEISHUI 或 BENBU）
         * @return 对应的校区，未匹配则返回渭水校区
         */
        fun fromName(name: String): Campus {
            return entries.find { it.name == name } ?: WEISHUI
        }
    }
}
